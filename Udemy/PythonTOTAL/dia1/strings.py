# String
"""
Cadena de texto o caracteres.
Se coloca entre comillas simples o dobles.
"""

# Concadenar
print("Hola" + "Cesar")

# Agregar espacio entre dos textos
print("Hola" + " " + "Cesar")
# Agregar el espacio al final de la primera entrada
print("Hola " + "Cesar")
# Agregar el espacio al inicio de la segunda entrada
print("Hola" + " Cesar")

# Comillas

# Comillas simples dentro de comillas dobles
print("Hola 'Cesar'")
# Comillas dobles dentro de comillas simples
print('Hola "Cesar"')

# Uso de barra
# Comillas simples dentro de comillas simples
print('Hola \'Cesar\'')
# Comillas dobles dentro de comillas dobles
print("Hola \"Cesar\"")
# \n es un salto de línea
print("Hola\nCesar")
# \t es un tabulador
print("Hola\tCesar")
# \\ es una barra invertida
print("Hola\\Cesar")

