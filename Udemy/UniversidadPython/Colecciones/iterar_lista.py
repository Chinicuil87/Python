print("Programa que muestra el uso de las listas para iterar elementos.")
lista = [1, 2, 3, 4, 5, 6]
print(f"Lista original: {lista}")

# Iterar con un for
for elemento in lista:
    print(f"Elemento: {elemento}")
    
nombres = ["Cesar", "Juan", "Maria", "Ana"]
# Iterar con un for con indice
for nombre in range(len(nombres)):
    print(f"Nombre: {nombres[nombre]}")
    
    