print("Programa que muestra el uso de las listas.")

lista = [1, 2, 3, 4, 5]
print(f"Lista original: {lista}")

# Largo de la lista
print(f"Largo de la lista: {len(lista)}")

# Acceso a elementos de la lista
print(f"Primer elemento: {lista[4]}") # Se recupera el elemento en la posicion 4 (5)
print(f"Ultimo elemento: {lista[-1]}") # Se recupera el ultimo elemento de la lista


