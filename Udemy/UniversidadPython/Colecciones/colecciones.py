# Una coleccion es un conjunto de elementos que se pueden almacenar y manipular en Python.
# Las colecciones mas comunes son las listas, tuplas, conjuntos y diccionarios.

# listas
# Una lista es una coleccion ordenada y mutable de elementos. Se pueden agregar, eliminar y modificar elementos en una lista.
# Las listas se crean utilizando corchetes [] y los elementos se separan por comas.
# Sintaxis: lista = [elemento1, elemento2, elemento3, ...]

print("Programa que muestra el uso de las listas.")
lista = [1, 2, 3, 4, 5]
print(f"Lista original: {lista}")

# Tuplas
# Una tupla es una coleccion ordenada e inmutable de elementos. No se pueden modificar los elementos de una tupla una vez que se ha creado.
# Las tuplas se crean utilizando par√©ntesis () y los elementos se separan por comas.
# Sintaxis: tupla = (elemento1, elemento2, elemento3, ...)
print("\nPrograma que muestra el uso de las tuplas.")
tupla = (1, 2, 3, 4, 5)
print(f"Tupla original: {tupla}")

# Conjuntos
# Un conjunto es una coleccion no ordenada y mutable de elementos. No se permiten elementos duplicados en un conjunto.
# Los conjuntos se crean utilizando llaves {} o la funcion set().
# Sintaxis: conjunto = {elemento1, elemento2, elemento3, ...}
print("\nPrograma que muestra el uso de los conjuntos.")
conjunto = {1, 2, 3, 4, 5}
print(f"Conjunto original: {conjunto}")

# Diccionarios
# Un diccionario es una coleccion no ordenada y mutable de pares clave-valor. Cada elemento del diccionario se compone de una clave y un valor asociado.
# Los diccionarios se crean utilizando llaves {} y los pares clave-valor se separan por comas.
# Sintaxis: diccionario = {clave1: valor1, clave2: valor2, ...}
print("\nPrograma que muestra el uso de los diccionarios.")
diccionario = {"clave1": "valor1", "clave2": "valor2", "clave3": "valor3"}
print(f"Diccionario original: {diccionario}")

