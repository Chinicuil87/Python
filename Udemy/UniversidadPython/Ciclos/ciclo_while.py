# Aplicacion de consola.

# Son estructuras de control que permiten repetir un bloque de codigo mientras se cumpla una condicion.
# Sintaxis:
# while condicion:
#     bloque de codigo

# Ejemplo de uso de la estructura while
contador = 0
while contador < 10:
    print("El contador es:", contador)
    contador += 1  # Incrementa el contador en 1
