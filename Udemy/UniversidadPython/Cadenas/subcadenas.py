# Una subcadena es la parte de una cadena.
# Podemos extraer subcadenas, buscarlas, contarlas y reemplazarlas.

# Para extraer una subcadena, se utiliza la funcion slice.
# Permite indicar el inicio y el fin de la subcadena.

# subcadena = cadena[inicio:fin]

cadena = "Hola Mundo"
subcadena = cadena[0:4]  # Hola
print("Subcadena: ", subcadena)  # Hola
subcadena = cadena[5:10]  # Mundo
print("Subcadena: ", subcadena)  # Mundo
