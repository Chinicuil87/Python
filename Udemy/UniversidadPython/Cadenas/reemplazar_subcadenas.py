# Metodo replace()
# Reemplaza una subcadena por otra en una cadena.
# La funcion replace() devuelve una nueva cadena con las subcadenas reemplazadas.
# La funcion replace() no modifica la cadena original.

cadena = "Hola Mundo"
nueva_cadena = cadena.replace("Mundo", "Python")
print("Cadena: ", cadena)  # Hola Mundo
print("Nueva cadena: ", nueva_cadena)  # Hola Python
print("Cadena original no se modifico: ", cadena)  # Hola Mundo
