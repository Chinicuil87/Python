# Aplicacion de consola.

# Permite unir dos o mas cadenas de texto.
# Se puede usar el operador + para unir cadenas de texto.
print("Hola" + " " + "Mundo")  # Hola Mundo

# Uso de join
print(" ".join(["Hola", "Mundo"]))  # Hola Mundo

cadena = "Hola "
cadena2 = "Mundo"
concadenar = cadena + cadena2  # Concatenar cadenas
print(concadenar)  # Hola Mundo
