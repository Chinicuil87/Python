# Aplicacion de consola.

# Los caracteres de una cadena esan indexados desde 0.
# Se puede aceder a un caracter indicando el indice al cual queremos acceder.
# Respetando el orden de los caracteres en los que se fueron ingresando

cadena = "Hola Mundo"
print("Primer caracter:", cadena[0])  # H
print("Segundo caracter:", cadena[1])  # o
print("Tercer caracter:", cadena[2])  # l
print("Cuarto caracter:", cadena[3])  # a
print("Quinto caracter:", cadena[4])  # " "
print("Sexto caracter:", cadena[5])  # M
print("Septimo caracter:", cadena[6])  # u
print("Octavo caracter:", cadena[7])  # n
print("Noveno caracter:", cadena[8])  # d
print("Decimo caracter:", cadena[9])  # o

# "Hola Mundo" tiene 10 caracteres, por lo que el indice maximo es 9.
