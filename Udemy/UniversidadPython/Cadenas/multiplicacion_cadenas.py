# Multiplicacion de cadenas
# La multiplicacion de cadenas se utiliza para repetir una cadena un numero determinado de veces.
# La multiplicacion de cadenas se utiliza para crear cadenas largas a partir de cadenas cortas.

texto = "Hola "
veces = 3
print("Texto: ", texto)  # Hola
print("Numero de veces: ", veces)  # 3
print("Multiplicacion de cadenas: ", texto * veces)  # Hola Hola Hola
