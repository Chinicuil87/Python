# Aplicacion de consola.

# Se pueden usar caracteres especiales intorducion una \ antes del caracter especial.
print("Hola Mundo")  # Hola Mundo
# # \n = nueva linea
print("Hola\nMundo")  # Hola
# # \t = tabulacion
print("\tHola Mundo")  # Hola    Mundo
# # \\ = barra invertida
print("Hola\\Mundo")  # Hola\Mundo
# # \' = comilla simple
print("Hola\'Mundo\'")  # Hola'Mundo'
# # \" = comilla doble
print("Hola\"Mundo\"")  # Hola"Mundo"
