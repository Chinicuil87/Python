# Aplicacion de consola.

#  Los operadores de comparacion son los siguientes:
#  ==  Igual que
#  !=  Diferente que
#  >   Mayor que
#  <   Menor que
#  >=  Mayor o igual que
# <=  Menor o igual que

# Los operadors de comparacion devuelven un valor booleano, es decir, True o False.

print("Operadores de comparacion")
print("===================================")

a = 7
b = 5

print(f'Valores iniales: a = {a}, b = {b}')
print("===================================")

# Operador de igualdad
print(f'Operador de igualdad: a == b: {a == b}')

# Operador de desigualdad
print(f'Operador de desigualdad: a != b: {a != b}')

# Operador mayor que
print(f'Operador mayor que: a > b: {a > b}')

# Operador menor que
print(f'Operador menor que: a < b: {a < b}')

# Operador mayor o igual que
print(f'Operador mayor o igual que: a >= b: {a >= b}')

# Operador menor o igual que
print(f'Operador menor o igual que: a <= b: {a <= b}')

print("===================================")
print("Fin del programa")
print("===================================")
