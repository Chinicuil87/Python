# Aplicacion de consola.

# nos permoten realizar operaciones matematicas.
# Suma: +
# Resta: -
# Multiplicacion: *
# Division: /
# Division entera: //
# Modulo: %
# Potencia: **
# Raiz cuadrada: **(1/2)
# Raiz cubica: **(1/3)

a = 10
b = 5

suma = a + b  # Suma
resta = a - b  # Resta
multiplicacion = a * b  # Multiplicacion
division = a / b  # Division
division_entera = a // b  # Division entera
modulo = a % b  # Modulo
potencia = a ** b  # Potencia
raiz_cuadrada = a ** (1 / 2)  # Raiz cuadrada

print("Suma: ", suma)  # 15
print("Resta: ", resta)  # 5
print("Multiplicacion: ", multiplicacion)  # 50
print("Division: ", division)  # 2.0
print("Division entera: ", division_entera)  # 2
print("Modulo: ", modulo)  # 0
print("Potencia: ", potencia)  # 100000
print("Raiz cuadrada: ", raiz_cuadrada)  # 3.1622776601683795
